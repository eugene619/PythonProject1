# Arithmetic Operators
num1 = 10
num2 = 3

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus- Returns the remainder after division

# Comparison Operators- Compare values
print(num1 < num2)
print(num1 > num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2) # Equal to
print(num1 != num2) # Not Equal

# Assignment Operators- Assign values to variables
x = 100
print(x)

x += 2
print(x)

y = 50
y -= 5
print(y)

# Logic Operators( and, or, not)
a = 23
print(a)
print(a < 40 and a > 2)
print(a < 40 or a < 2)
print(not(a < 40 and a > 2))

#Operator Precedence- Order in which operations get executed
result = (10+6/2-4)
print(result)
print(int(result))

