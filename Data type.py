
# Int
number = 60
# Float
num = 45.36
# Str
greeting = "Hello there"
# Boolean
isPythoninteresting = True
# List
fruits = ["banana", "mango", "apple"]
no = [1, 2, 3, 4, 5]
# Tuple
cars = ("bmw", "mercedes", "toyota", "audi")
# Set
countries = {"kenya", "tanzania", "uganda"}
# Dictionary
student = {
    "firstname": "Gold",
    "age": 19,
    "course": "MIT",
    "nationality": "kenyan"
}

print(num)
print(isPythoninteresting)
print(fruits[1])
print(no)
print(cars)
print(countries)
print(student["firstname"])
print(student["course"])

#Determining Data type
print(type(isPythoninteresting))
print(type(cars))
print(type(countries))
print(type(fruits))
print(type(greeting))

#Typecasting is converting one datatype to another
print(int(num))
print(float(number))
print(bool(no))
