text = "Hello world"
course = "WEB DEVELOPMENT"

# Accessing an element in a string
print(text[0])
print(text[5])

# Determining the size of a string
print(len(text))

# Modifying a string
print(text.upper())
print(course.lower())

# String concatenation- Joining strings
first = "Hello there"
name = "Olivia"
print(first+name)
# Spacing
print(first+" "+name)

result = f"{first} {name}"
print(result)

